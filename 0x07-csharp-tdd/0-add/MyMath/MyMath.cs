using System;

namespace MyMath
{
    /// <summary>
    /// Operations class
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Adds two numbers
        /// </summary>
        /// <param name="a">number one</param>
        /// <param name="b">number two</param>
        /// <returns>sum of a and b</returns>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}
